/**
 * Calculate force toward nearest point in the shape
 */
export function amplifierShapeForce(
  px: number,
  py: number,
  strength: number = 1
): [number, number] {
  const points = getAmplifierShapePoints()
  
  let minDist = Infinity
  let nearestX = px
  let nearestY = py
  
  for (const {x, y} of points) {
    const dist = Math.sqrt((px - x) ** 2 + (py - y) ** 2)
    if (dist < minDist) {
      minDist = dist
      nearestX = x
      nearestY = y
    }
  }
  
  // Force direction toward nearest point
  let fx = nearestX - px
  let fy = nearestY - py
  
  const mag = Math.sqrt(fx * fx + fy * fy)
  if (mag > 0.001) {
    fx /= mag
    fy /= mag
  }
  
  // Force magnitude - stronger when further, caps at threshold
  const forceMag = Math.min(minDist * 3, 0.5) * strength
  
  return [fx * forceMag, fy * forceMag]
}
